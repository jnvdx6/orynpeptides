export default function AdminLayout({ children }: { children: React.ReactNode }) {
  const router = useRouter();
  const pathname = usePathname();
  const [isAuthed, setIsAuthed] = useState(false);
  const [sidebarOpen, setSidebarOpen] = useState(false);
  const [adminEmail, setAdminEmail] = useState('');

  useEffect(() => {
    const token = localStorage.getItem('oryn_admin_token');
    const email = localStorage.getItem('oryn_admin_email');
    if (!token && pathname !== '/admin/login') {
      router.push('/admin/login');
    } else {
      setIsAuthed(true);
      setAdminEmail(email || 'Admin');
    }
  }, [pathname, router]);

  const handleLogout = () => {
    localStorage.removeItem('oryn_admin_token');
    localStorage.removeItem('oryn_admin_email');
    router.push('/admin/login');
  };

  if (pathname === '/admin/login') {
    return (
      <html lang="en">
        <body className="font-grotesk antialiased">{children}</body>
      </html>
    );
  }

  if (!isAuthed) {
    return (
      <html lang="en">
        <body className="font-grotesk antialiased">
          <div className="min-h-screen bg-gray-50 flex items-center justify-center">
            <div className="flex items-center gap-2 text-gray-400">
              <svg className="w-4 h-4 animate-spin" fill="none" viewBox="0 0 24 24">
                <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4" />
                <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4z" />
              </svg>
              <span className="text-sm">Loading...</span>
            </div>
          </div>
        </body>
      </html>
    );
  }

  const isActive = (href: string) => {
    if (href === '/admin') return pathname === '/admin';
    return pathname.startsWith(href);
  };

  const getPageTitle = () => {
    const titles: Record<string, string> = {
      '/admin': 'Dashboard',
      '/admin/orders': 'Orders',
      '/admin/products': 'Products',
      '/admin/promotions': 'Promotions',
      '/admin/users': 'Customers',
      '/admin/referrals': 'Referral Network',
      '/admin/commissions': 'Commissions',
      '/admin/payments': 'Crypto Payments',
      '/admin/analytics': 'Analytics',
      '/admin/medusa-orders': 'Medusa Orders',
      '/admin/settings': 'Settings',
    };
    return titles[pathname] || 'Admin';
  };

  return (
    <html lang="en">
    <body className="font-grotesk antialiased">
    <div className="min-h-screen bg-gray-50 flex">
      {/* Mobile overlay */}
      {sidebarOpen && (
        <div
          className="fixed inset-0 bg-black/20 backdrop-blur-sm z-40 lg:hidden transition-opacity"
          onClick={() => setSidebarOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`fixed lg:static inset-y-0 left-0 z-50 w-[240px] bg-white border-r border-gray-200 transform transition-transform duration-200 ease-in-out ${
          sidebarOpen ? 'translate-x-0' : '-translate-x-full lg:translate-x-0'
        } flex flex-col`}
      >
        {/* Logo */}
        <div className="h-14 flex items-center px-5 shrink-0">
          <Link href="/admin" className="flex items-center gap-2">
            <div className="w-7 h-7 rounded-lg bg-oryn-orange flex items-center justify-center">
              <span className="text-white font-bold text-xs">O</span>
            </div>
            <span className="text-gray-900 font-semibold text-[15px]">Oryn Admin</span>
          </Link>
        </div>

        {/* Nav */}
        <nav className="flex-1 overflow-y-auto px-3 pb-4">
          {navSections.map((section, si) => (
            <div key={si} className={section.label ? 'mt-6' : 'mt-1'}>
              {section.label && (
                <div className="px-3 mb-1.5">
                  <span className="text-[11px] font-medium text-gray-400 uppercase tracking-wider">{section.label}</span>
                </div>
              )}
              <div className="space-y-0.5">
                {section.items.map((item) => (
                  <Link
                    key={item.href}
                    href={item.href}
                    onClick={() => setSidebarOpen(false)}
                    className={`flex items-center gap-2.5 px-3 py-[7px] rounded-lg text-[13px] font-medium transition-colors ${
                      isActive(item.href)
                        ? 'bg-gray-100 text-gray-900'
                        : 'text-gray-500 hover:bg-gray-50 hover:text-gray-700'
                    }`}
                  >
                    <span className={isActive(item.href) ? 'text-gray-700' : 'text-gray-400'}>{item.icon}</span>
                    {item.label}
                  </Link>
                ))}
              </div>
            </div>
          ))}
        </nav>

        {/* Sidebar footer - User */}
        <div className="border-t border-gray-100 p-3">
          <div className="flex items-center gap-2.5 px-2 py-1.5">
            <div className="w-7 h-7 rounded-full bg-gray-100 flex items-center justify-center shrink-0">
              <span className="text-[11px] font-semibold text-gray-500">
                {adminEmail.charAt(0).toUpperCase()}
              </span>
            </div>
            <div className="flex-1 min-w-0">
              <p className="text-[12px] font-medium text-gray-700 truncate">{adminEmail}</p>
            </div>
            <button
              onClick={handleLogout}
              className="p-1 rounded text-gray-400 hover:text-gray-600 hover:bg-gray-100 transition-colors"
              title="Sign out"
            >
              <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
                <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 9V5.25A2.25 2.25 0 0013.5 3h-6a2.25 2.25 0 00-2.25 2.25v13.5A2.25 2.25 0 007.5 21h6a2.25 2.25 0 002.25-2.25V15m3 0l3-3m0 0l-3-3m3 3H9" />
              </svg>
            </button>
          </div>
        </div>
      </aside>

      {/* Main content */}
      <div className="flex-1 flex flex-col min-h-screen min-w-0">
        {/* Top bar */}
        <header className="h-14 bg-white border-b border-gray-200 flex items-center justify-between px-4 lg:px-6 sticky top-0 z-30">
          <div className="flex items-center gap-3">
            <button
              onClick={() => setSidebarOpen(true)}
              className="lg:hidden p-1.5 rounded-lg hover:bg-gray-100 transition-colors text-gray-500"
            >
              <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
                <path strokeLinecap="round" strokeLinejoin="round" d="M3.75 6.75h16.5M3.75 12h16.5m-16.5 5.25h16.5" />
              </svg>
            </button>
            <h1 className="text-[15px] font-semibold text-gray-900">{getPageTitle()}</h1>
          </div>

          <div className="flex items-center gap-2">
            {/* CSV Export button — visible on exportable pages */}
            {['/admin/orders', '/admin/users', '/admin/commissions'].includes(pathname) && (
              <button
                onClick={() => {
                  const resourceMap: Record<string, string> = {
                    '/admin/orders': 'orders',
                    '/admin/users': 'customers',
                    '/admin/commissions': 'commissions',
                  };
                  const resource = resourceMap[pathname];
                  if (!resource) return;
                  const token = localStorage.getItem('oryn_admin_token');
                  window.open(`/api/admin/export?resource=${resource}&token=${token}`, '_blank');
                }}
                className="flex items-center gap-1.5 px-2.5 py-1.5 rounded-lg hover:bg-gray-100 transition-colors text-gray-500 text-[12px] font-medium"
                title="Export CSV"
              >
                <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
                  <path strokeLinecap="round" strokeLinejoin="round" d="M3 16.5v2.25A2.25 2.25 0 005.25 21h13.5A2.25 2.25 0 0021 18.75V16.5M16.5 12L12 16.5m0 0L7.5 12m4.5 4.5V3" />
                </svg>
                Export CSV
              </button>
            )}
            <button className="p-1.5 rounded-lg hover:bg-gray-100 transition-colors text-gray-400">
              <svg className="w-[18px] h-[18px]" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
                <path strokeLinecap="round" strokeLinejoin="round" d="M14.857 17.082a23.848 23.848 0 005.454-1.31A8.967 8.967 0 0118 9.75v-.7V9A6 6 0 006 9v.75a8.967 8.967 0 01-2.312 6.022c1.733.64 3.56 1.085 5.455 1.31m5.714 0a24.255 24.255 0 01-5.714 0m5.714 0a3 3 0 11-5.714 0" />
              </svg>
            </button>
          </div>
        </header>

        {/* Page content */}
        <main className="flex-1 p-4 lg:p-6">
          {children}
        </main>
      </div>
    </div>
    </body>
    </html>
  );
}
